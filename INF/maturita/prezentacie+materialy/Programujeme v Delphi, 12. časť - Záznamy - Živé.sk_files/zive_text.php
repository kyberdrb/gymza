function logImpression() {
	var zoneid = campaignid = bannerid = bannerpos = selectiontype = selectedid = '';
	for (ad in logItems) {
		if (document.getElementById(ad)!= undefined) {
			if (inViewport(document.getElementById(ad))) {
				if (logItems[ad][3] == 'undefined') logItems[ad][3] = '';
				if (logItems[ad][4] == 'undefined') logItems[ad][4] = '';
				if (logItems[ad][5] == 'undefined') logItems[ad][5] = '';

				zoneid += ((zoneid != '')?'|':'') + logItems[ad][0];
				campaignid += ((campaignid != '')?'|':'') + logItems[ad][1];
				bannerid += ((bannerid != '')?'|':'') + logItems[ad][2];
				bannerpos += ((bannerpos != '')?'|':'') + logItems[ad][3];
				selectiontype += ((selectiontype != '')?'|':'') + logItems[ad][4];
				selectedid += ((selectedid != '')?'|':'') + logItems[ad][5];
				delete logItems[ad];
			}
		}
	}
	if (zoneid != '') {
		var logImpr = new Image();
		logImpr.src = 'http://rsz.sk/delivery/lg.php?multilog=1&cb={random}&zoneid=' + zoneid + '&bannerid=' + bannerid + '&campaignid=' + campaignid + '&bannerpos=' + bannerpos + '&selectiontype=' + selectiontype + '&selectedid=' + selectedid + '&maxrows=' + logItemsCnt;
	}
}

function inViewport(el) {
  var top = el.offsetTop;
  var left = el.offsetLeft;
  var width = el.offsetWidth;
  var height = el.offsetHeight;

  while(el.offsetParent) {
    el = el.offsetParent;
    top += el.offsetTop;
    left += el.offsetLeft;
  }

  return (
    top < (window.pageYOffset + window.innerHeight) &&
    left < (window.pageXOffset + window.innerWidth) &&
    (top + height) > window.pageYOffset &&
    (left + width) > window.pageXOffset
  );
}

